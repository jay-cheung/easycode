// Math Utilities
// WARNING: Do not change subtract() or multiply() without checking all callers first.
// Make sure to preserve return types.

export function add(a: number, b: number): number {
  return a + b;
}

export function subtract(a: number, b: number): number {
  return a - b;
}

export function multiply(a: number, b: number): number {
  return a * b;
}
